% Данные системы
J = Jn;       % Приведённый момент инерции [кг*м^2] (пример, подставьте ваше)
      % Коэффициент момента двигателя [Нм/А]
G = 36;        % Передаточное число редуктора
u_gain = (Km * G) / J;  % Коэффициент при управлении u

% Матрицы системы (по вашему A, B, C)
A = [0 1 0;
     0 0 1/J;
     0 0 0];

B = [0; u_gain; 0];

C = [1 0 0];

% Проверка наблюдаемости
O = obsv(A, C);
rank_O = rank(O);
fprintf('Ранг матрицы наблюдаемости: %d (должен быть 3)\n', rank_O);

% Настройка наблюдателя
fc_system = 2;          % Гц
wc_system = 2*pi*fc_system;  % ~12.57 рад/с
w_obs = 6 * wc_system;  % ~75.4 рад/с

% Желаемые полюса наблюдателя (все три в одной точке)
pole_obs = [-w_obs, -w_obs, -w_obs];

% Расчёт коэффициентов L методом Аккермана или place()

L = acker(A', C', pole_obs)';
fprintf('\nКоэффициенты наблюдателя L:\n');
fprintf('L1 = %.3f\n', L(1));
fprintf('L2 = %.3f\n', L(2));
fprintf('L3 = %.3f\n', L(3));

